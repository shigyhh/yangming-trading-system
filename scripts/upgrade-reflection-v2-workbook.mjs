function columnName(index) {
  let name = ""
  let value = index
  while (value > 0) {
    const mod = (value - 1) % 26
    name = String.fromCharCode(65 + mod) + name
    value = Math.floor((value - mod) / 26)
  }
  return name
}

function matrixRange(sheetName, rowCount, colCount) {
  return `${sheetName}!A1:${columnName(colCount)}${rowCount}`
}

function sceneNumber(scene) {
  return String(scene.sceneOrder).padStart(2, "0")
}

async function loadScenes() {
  const files = (await fs.readdir(scenesDir))
    .filter((file) => /^scene-\d+-.+\.json$/.test(file))
    .sort()

  const scenes = []
  for (const file of files) {
    const scene = JSON.parse(await fs.readFile(path.join(scenesDir, file), "utf8"))
    scenes.push({ ...scene, sourceFile: `web-next/src/data/insight-engine/scenes/${file}` })
  }

  return scenes.sort((a, b) => a.sceneOrder - b.sceneOrder)
}

function buildReason(sceneId) {
  return `不再停在表层念，直接照到「${painByScene[sceneId]}」。`
}

function gradeReflection(text) {
  if (text.includes("羞耻") || text.includes("体面") || text.includes("旁观") || text.includes("证明") || text.includes("拿不住")) {
    return 5
  }
  return 4
}

function validateUpgrade(scenes) {
  const issues = []
  for (const scene of scenes) {
    const reflections = reflectionV2ByScene[scene.sceneId]
    if (!painByScene[scene.sceneId]) issues.push(`${scene.sceneId} missing pain`)
    if (!reflections || reflections.length !== scene.items.length) {
      issues.push(`${scene.sceneId} reflection count ${reflections?.length ?? 0} != ${scene.items.length}`)
      continue
    }

    reflections.forEach((text, index) => {
      if (!text || text.trim().length < 8) issues.push(`${scene.sceneId} #${index + 1} empty`)
      for (const term of forbiddenReflectionTerms) {
        if (text.includes(term)) issues.push(`${scene.sceneId} #${index + 1} contains forbidden term ${term}`)
      }
      if (text === scene.items[index].reflection) issues.push(`${scene.sceneId} #${index + 1} duplicates original reflection`)
      if (text.includes(scene.items[index].hiddenThought)) issues.push(`${scene.sceneId} #${index + 1} exposes hiddenThought`)
      if (gradeReflection(text) < 4) issues.push(`${scene.sceneId} #${index + 1} grade below 4`)
    })
  }
  return issues
}

function buildRows(scenes) {
  const detailRows = []
  const sceneRows = []
  const sourceRows = []

  for (const scene of scenes) {
    const sceneNo = sceneNumber(scene)
    const mirrorName = mirrorNames[scene.mirrorId] || scene.mirrorId

    sceneRows.push([
      sceneNo,
      scene.sceneId,
      scene.sceneName,
      scene.mirrorId,
      mirrorName,
      scene.thief,
      scene.items.length,
      scene.evidences.length,
      scene.practices.length,
      scene.coreStatement,
      scene.status,
      scene.sourceFile,
    ])

    scene.thiefExplain.forEach((text, index) => {
      sourceRows.push([sceneNo, scene.sceneId, scene.sceneName, "心贼解释", index + 1, text])
    })
    scene.evidences.forEach((text, index) => {
      sourceRows.push([sceneNo, scene.sceneId, scene.sceneName, "心证", index + 1, text])
    })
    scene.practices.forEach((text, index) => {
      sourceRows.push([sceneNo, scene.sceneId, scene.sceneName, "修行", index + 1, text])
    })

    scene.items.forEach((item, index) => {
      const evidence = scene.evidences[index % scene.evidences.length] || ""
      const practice = scene.practices[index % scene.practices.length] || ""
      const itemNo = String(index + 1).padStart(2, "0")
      const reflectionV2 = reflectionV2ByScene[scene.sceneId][index]
      const pain = painByScene[scene.sceneId]

      detailRows.push([
        `${sceneNo}-${itemNo}`,
        sceneNo,
        scene.sceneId,
        scene.sceneName,
        scene.mirrorId,
        mirrorName,
        scene.thief,
        item.id,
        item.tradeMoment,
        item.os,
        item.hiddenThought,
        item.reflection,
        reflectionV2,
        pain,
        buildReason(scene.sceneId),
        gradeReflection(reflectionV2),
        scene.thiefExplain.join("\n"),
        evidence,
        practice,
        scene.coreStatement,
        item.intensity,
        scene.status,
        complianceNote,
        scene.sourceFile,
        "可调整",
        "",
      ])
    })
  }

  return { detailRows, sceneRows, sourceRows }
}

function setValues(sheet, sheetName, headers, rows) {
  const values = [headers, ...rows]
  sheet.getRange(matrixRange(sheetName, values.length, headers.length)).values = values
}

