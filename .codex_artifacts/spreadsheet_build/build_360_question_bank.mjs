function add(personality, subDimension, questions, sceneTag) {
  const meta = dimensionMeta[personality];
  const currentCount = bank.filter((item) => item.personality === personality).length;
  questions.forEach((question, idx) => {
    const seq = currentCount + idx + 1;
    const id = `${meta.prefix}${String(seq).padStart(3, "0")}`;
    bank.push({
      id,
      personality,
      nature: meta.nature,
      subDimension,
      question,
      scale: "1-5分：1完全不像我，2偶尔如此，3有时如此，4经常如此，5几乎就是我",
      direction: "正向计分",
      weight: 1,
      sceneTag,
      reportTag: `${personality}-${subDimension}`,
      version: seq <= 3 ? "27题/45题/90题/180题/360题" : seq <= 5 ? "45题/90题/180题/360题" : seq <= 10 ? "90题/180题/360题" : "180题/360题",
      coreLevel: seq <= 3 ? "核心题" : seq <= 10 ? "标准题" : "扩展题",
      ability: meta.ability,
      ymReminder: meta.ym,
      compliance: "仅用于交易认知教育、行为觉察和训练分层，不构成投资建议。",
    });
  });
}

